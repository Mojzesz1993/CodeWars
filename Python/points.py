def points(games):
    scores = 0
    for i in range (0, len(games)):
        s1 = games[i][:1]
        s2 = games[i][2:]

        if int(s1) > int(s2):
            scores += 3
        elif int(s1) == int(s2):
            scores += 1

    scores = int(scores)
    print (scores)
    return scores








